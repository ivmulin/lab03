#include "rsq.hpp"
#include <climits>
#include <iostream>
#include <vector>

int main() {
  // Исходный тестовый массив данных
  std::vector<int> data = {3, 1, 4, 1, 5, 9, 2, 6, 5};

  std::cout << "--- ТЕСТ 1: RSQ (Сумма элементов) ---\n";
  // Инициализируем структуру лямбдой сложения и нейтральным элементом 0
  auto sum_op = [](int a, int b) { return a + b; };
  SqrtDecomposition rsq(data, sum_op, 0);

  // Запросы на отрезках
  std::cout << "Сумма на отрезке [0, 2] (3+1+4): " << rsq.query(0, 2)
            << " (Ожидается: 8)\n";
  std::cout << "Сумма на отрезке [1, 7] (1+4+1+5+9+2+6): " << rsq.query(1, 7)
            << " (Ожидается: 28)\n";
  std::cout << "Сумма внутри одного блока [4, 5] (5+9): " << rsq.query(4, 5)
            << " (Ожидается: 14)\n";

  // Тестируем обновление элемента
  std::cout << "\nОбновляем элемент с индексом 3: меняем значение 1 на 10...\n";
  rsq.update(3, 10); // Массив стал: {3, 1, 4, 10, 5, 9, 2, 6, 5}
  std::cout << "Новая сумма на отрезке: " << rsq.query(1, 7)
            << " (Ожидается: 37)\n";

  std::cout << "\n--- ТЕСТ 2: RMQ (Минимум) ---\n";
  // Инициализируем ту же структуру лямбдой минимума и нейтральной
  // бесконечностью
  auto min_op = [](int a, int b) { return std::min(a, b); };
  SqrtDecomposition rmq(data, min_op, INT_MAX);

  // Запросы на отрезках
  std::cout << "Минимум на отрезке [0, 2] (из {3, 1, 4}): " << rmq.query(0, 2)
            << " (Ожидается: 1)\n";
  std::cout << "Минимум на отрезке [4, 7] (из {5, 9, 2, 6}): "
            << rmq.query(4, 7) << " (Ожидается: 2)\n";

  // Тестируем обновление элемента
  std::cout << "\nОбновляем элемент с индексом 6: меняем минимум 2 на 20...\n";
  rmq.update(6, 20); // Массив стал: {3, 1, 4, 1, 5, 9, 20, 6, 5}
  std::cout << "Новый минимум на отрезке [4, 7] (из {5, 9, 20, 6}): "
            << rmq.query(4, 7) << " (Ожидается: 5)\n";

  return 0;
}
